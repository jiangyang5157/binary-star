#!/bin/bash
# в”Җв”Җв”Җ Claude Code Status Line в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#   crypto v26.6.27 в”Ӯ вҸұ 2h в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–‘в–‘в–‘вҡ  вҶ“173.2k/200k вҶ‘12.5k в”Ӯ deepseek-v4-pro в—Ҷxhigh
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

set -euo pipefail
input=$(cat)

# в”Җв”Җ JSON еӯ—ж®ө в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
REPO=$(echo "$input" | jq -r '.workspace.repo.name // ""')
DIR=$(echo "$input" | jq -r '.workspace.current_dir')
PCT=$(echo "$input" | jq -r '.context_window.used_percentage // 0' | cut -d. -f1)
DUR_MS=$(echo "$input" | jq -r '.cost.total_duration_ms // 0')
EFFORT=$(echo "$input" | jq -r '.effort.level // ""')
TOTAL_IN=$(echo "$input" | jq -r '.context_window.total_input_tokens // 0')
TOTAL_OUT=$(echo "$input" | jq -r '.context_window.total_output_tokens // 0')
WIN_SIZE=$(echo "$input" | jq -r '.context_window.context_window_size // 0')
EXCEEDS=$(echo "$input" | jq -r '.exceeds_200k_tokens // false')

# в”Җв”Җ жЁЎеһӢеҗҚпјҡдјҳе…Ҳд»ҺзҺҜеўғеҸҳйҮҸеҸ–зңҹе®һеҗҚз§°пјҢеӣһйҖҖеҲ° display_name в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
MODEL="${ANTHROPIC_MODEL:-}"
[ -z "$MODEL" ] && MODEL=$(echo "$input" | jq -r '.model.display_name')

# в”Җв”Җ еӣһйҖҖ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
[ -z "$REPO" ] && REPO="${DIR##*/}"

# в”Җв”Җ Git еҲҶж”ҜпјҲйқһ repo еҲҷйҡҗи—Ҹпјүв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
BRANCH=$(git branch --show-current 2>/dev/null || echo "")

# в”Җв”Җ ANSI йўңиүІ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
C_RESET='\033[0m'
C_DIM='\033[2m'
C_GREEN='\033[32m'
C_YELLOW='\033[33m'
C_RED='\033[31m'
C_CYAN='\033[36m'
C_MAGENTA='\033[35m'

# в”Җв”Җ иҝӣеәҰжқЎ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
FILLED=$((PCT / 10))
EMPTY=$((10 - FILLED))
BAR=""
i=0; while [ $i -lt $FILLED ]; do BAR="${BAR}в–Ҳ"; i=$((i + 1)); done
i=0; while [ $i -lt $EMPTY ];  do BAR="${BAR}в–‘"; i=$((i + 1)); done

if   [ "$PCT" -ge 90 ]; then BAR_COLOR="$C_RED"
elif [ "$PCT" -ge 70 ]; then BAR_COLOR="$C_YELLOW"
else BAR_COLOR="$C_GREEN"
fi

# в”Җв”Җ и¶… 200k ж Үи®° в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
EXCEEDS_ICON=""
[ "$EXCEEDS" = "true" ] && EXCEEDS_ICON="вҡ "

# в”Җв”Җ ж—¶й•ҝж јејҸеҢ– в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
DUR_SEC=$((DUR_MS / 1000))
DUR_MIN=$((DUR_SEC / 60))
DUR_H=$((DUR_MIN / 60))
DUR_M=$((DUR_MIN % 60))
if [ "$DUR_H" -gt 0 ]; then
    DUR_FMT="${DUR_H}h"
    [ "$DUR_M" -gt 0 ] && DUR_FMT="${DUR_H}h${DUR_M}m"
else
    DUR_FMT="${DUR_M}m"
fi

# в”Җв”Җ Effort icon в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
EFFORT_ICON=""
case "$EFFORT" in
  ultracode) EFFORT_ICON="вҡЎвҡЎ" ;;
  max)       EFFORT_ICON="вҡЎ" ;;
  xhigh)     EFFORT_ICON="в—Ҷ " ;;
  high)      EFFORT_ICON="в—Ү " ;;
  medium)    EFFORT_ICON="в—Ұ " ;;
  low)       EFFORT_ICON="В· " ;;
esac

# в”Җв”Җ жһ„е»әз¬¬ 1 ж®өпјҡиә«д»Ҫ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
SEG1="${C_CYAN}${REPO}${C_RESET}"
[ -n "$BRANCH" ] && SEG1="${SEG1} ${C_MAGENTA}${BRANCH}${C_RESET}"

# в”Җв”Җ Token ж јејҸеҢ–пјҲ12500 вҶ’ 12.5kпјүв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
fmt_tok() {
    local n=$1
    if [ "$n" -ge 1000000 ]; then
        printf "%.1fM" "$(echo "scale=1; $n/1000000" | bc)"
    elif [ "$n" -ge 1000 ]; then
        printf "%.1fk" "$(echo "scale=1; $n/1000" | bc)"
    else
        printf "%d" "$n"
    fi
}

# в”Җв”Җ жһ„е»әз¬¬ 2 ж®өпјҡж—¶й•ҝ + дёҠдёӢж–Ү в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
TOK_IN_FMT=$(fmt_tok "$TOTAL_IN")
TOK_OUT_FMT=$(fmt_tok "$TOTAL_OUT")
TOK_MAX_FMT=$(fmt_tok "$WIN_SIZE")
SEG2="вҸұ ${DUR_FMT}  ${BAR_COLOR}${BAR} ${EXCEEDS_ICON} вҮЈ${TOK_IN_FMT}/${TOK_MAX_FMT} вҮЎ${TOK_OUT_FMT}${C_RESET}"

# в”Җв”Җ жһ„е»әз¬¬ 3 ж®өпјҡжЁЎеһӢ + effort в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
SEG3="${MODEL} ${EFFORT_ICON}${EFFORT}"

# в”Җв”Җ жёІжҹ“ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
printf "%b" "${SEG1}  в”Ӯ  ${SEG2}  в”Ӯ  ${SEG3}"
echo
